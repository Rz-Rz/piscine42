int	ft_strcmp(char *s1, char *s2);

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	a1;
	int	a2;

	i = 0;
	while (1 == 1)
	{
		if (s1[i] == '\0' && s2[i] == '\0')
			return (0);
		a1 = (int)s1[i];
		a2 = (int)s2[i];
		if (a1 > a2)
			return (1);
		if (a1 < a2)
			return (-1);
		i++;
	}
}
